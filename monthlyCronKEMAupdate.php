	function lead_reports_kema() {
		$startDate = new DateTime('first day of last month');
		$endDate = new DateTime('last day of last month');

		$sql = "SELECT latitude, longitude, iPostcode, iCity, systemDetails FROM leads WHERE created >= '{$startDate->format('Y-m-d')}' AND created < '{$endDate->modify('+1 day')->format('Y-m-d')}' AND status = 'dispatched'";
		$leads = db_query($sql);

		if (!$leads || $leads->num_rows == 0)
			return false;

		return $leads;
	}

	function doSQWPConnect() {
		global $db_sqwp_host, $db_sqwp_user, $db_sqwp_pass, $db_sqwp_name, $nowSql, $_connection;
		$_connection = mysqli_connect($db_sqwp_host, $db_sqwp_user, $db_sqwp_pass, $db_sqwp_name);
		if (!$_connection) {
			echo "Error: Unable to connect to MySQL." . PHP_EOL;
			echo "Debugging errno: " . mysqli_connect_errno() . PHP_EOL;
			echo "Debugging error: " . mysqli_connect_error() . PHP_EOL;
			exit;
		}
		date_default_timezone_set('Australia/Adelaide');
		mysqli_query($_connection, "SET time_zone='+0:00'");
		putenv("TZ=Australia/Adelaide");
		$nowSql = "DATE_ADD(NOW(), INTERVAL " . date('Z') . " SECOND)";
	}

	function getCerCsv($url, $file){
		global $filePath;

		// Get headers for the URL & Check if the headers indicate a successful response
		$headers = @get_headers($url);
		if ($headers && strpos($headers[0], '200 OK') === false)
			return false;

		$data = file_get_contents($url);
		if($data === false)
			return false;
		file_put_contents($filePath.'/'.$file, $data);

		return $filePath.'/'.$file;
	}

	function processCsv($csvFile, $arrayKey){
		global $techEmail, $techName;
		$handle = fopen($csvFile, 'r');

		$sumOutput = [];
		$outputColumns = [];

		// Check if the file is opened successfully
		if ($handle !== FALSE) {
			// Read the first row (header) to identify the relevant columns and get the column numbers
			$headers = fgetcsv($handle);
			foreach ($headers as $index => $header) {
				if (strpos($header, 'Rated Power Output In kW') !== false || strpos($header, 'Installation Quantity') !== false) {
					$outputColumns[] = $index;
				}
			}
			if (empty($outputColumns)){
				SendMail($techEmail, $techName, "KEMA update error", "KEMA csv's column headers not found");
				die("Error occurred: KEMA csv's column headers not found\n");
			}

			// Loop through each row of the CSV
			while (($data = fgetcsv($handle)) !== FALSE) {
				if(is_null($data[0]) || $data[0] == '')
					continue;
				$postcode = $data[0]; // Assuming postcode is the first column

				if (!array_key_exists($postcode, $sumOutput)) {
					$sumOutput[$postcode] = [$arrayKey => '0'];
				}

				// Sum the outputs from thr monthly columns
				foreach ($outputColumns as $columnIndex) {
					$sumOutput[$postcode][$arrayKey] += floatval($data[$columnIndex]);
				}
			}

			fclose($handle);
			return $sumOutput;
		} else {
			echo "Failed to open the file.";
			return false;
		}
	}

